'1、调整名字的大小写（用变量表示一个人的名字，再以小写、大写和首字母大写的方式显示这个人的名字）'
name = 'Eric'
problem = 'would you like to learn some Python today?'
message_lower = f'Hello {name.lower()},{problem}'
message_upper = f'Hello {name.upper()},{problem}'
message_title = f'Hello {name.title()},{problem}'
print(message_lower)
print(message_upper)
print(message_title)

